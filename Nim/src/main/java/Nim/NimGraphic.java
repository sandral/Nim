    private void pressButton(int i) {
        
        if (state == i-1) {
            stateChosen++;
        } else {
            state = i-1;
            stateChosen = 1;
        }
        
        if (application.stackSizes()[state]-stateChosen<0) {
            stateChosen = 0;
        }
        
        updateStacks();
    }
