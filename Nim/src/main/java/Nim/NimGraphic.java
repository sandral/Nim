    private void updateStacks() {
        int[] ss = application.stackSizes();
        if (state>=0) {
            ss[state]-=stateChosen;
        }
        
        for (int i = 0; i < 5; i++) {
            int sum = 0;
            for (int j = 0; j < 10; j++) {
                if (j < ss[i]) {
                    stacks[i][j].setVisible(true);
                    sum++;
                } else {
                    stacks[i][j].setVisible(false);
                }
            }
        }
    }

